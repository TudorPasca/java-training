package code._4_student_effort.challenge_2;

public class NumberToWord {
    private int number;
    private String word;

    public NumberToWord(int number, String word) {
        this.number = number;
        this.word = word;
    }

    public int getNumber() {
        return number;
    }

    public String getWord() {
        return word;
    }
}
